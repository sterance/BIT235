public class ex2 {
    public static void main(String[] args) {
        int a = 12;
        int b = 5;
        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a%b));
        System.out.println("Division rounds down to the nearest integer because both a and b are declared as ints");
    }
}